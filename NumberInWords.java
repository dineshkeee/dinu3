package set4;
import java.util.*;
public class NumberInWords {
	public static void main(String args[]){
		String arr[]={"one","two","three","four","five","six","seven","eight","nine","ten"};
		Scanner in=new Scanner(System.in);
		String a=in.next();
		switch(a){
		case "1":
			System.out.println(arr[0]);break;
		case "2":
			System.out.println(arr[1]);break;
		case "3":
			System.out.println(arr[2]);break;
		case "4":
			System.out.println(arr[3]);break;
		case "5":
			System.out.println(arr[4]);break;
		case "6":
			System.out.println(arr[5]);break;
		case "7":
			System.out.println(arr[6]);break;
		case "8":
			System.out.println(arr[7]);break;
		case "9":
			System.out.println(arr[8]);break;
		case "10":
			System.out.println(arr[9]);break;
	}
	}
}
